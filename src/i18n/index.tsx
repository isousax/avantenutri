interface I18nContextValue {
  locale: Locale;
  t: (key: TranslationKey, vars?: Record<string,string|number>) => string;
  setLocale: (l: Locale) => void;
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export const I18nProvider: React.FC<{ defaultLocale?: Locale; children: React.ReactNode; }>=({defaultLocale='pt', children})=> {
  const [locale, setLocale] = useState<Locale>(()=> {
    const stored = typeof window!=='undefined'? localStorage.getItem('locale'): null;
    return (stored==='pt'|| stored==='en')? stored : defaultLocale;
  });
  useEffect(()=> { try { localStorage.setItem('locale', locale);} catch {} }, [locale]);
  const value = useMemo(()=> ({
    locale,
    setLocale,
    t: (key: TranslationKey, vars?: Record<string,string|number>) => {
      const dict = translations[locale] || {};
      let phrase = dict[key] || key;
      if (vars) {
        Object.entries(vars).forEach(([k,v])=>{
          phrase = phrase.replace(new RegExp(`{${k}}`, 'g'), String(v));
        });
      }
      return phrase;
    }
  }), [locale]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export function useI18n() {
  const ctx = useContext(I18nContext);
  if(!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
}

export function formatNumber(num: number, locale: Locale){
  return new Intl.NumberFormat(locale === 'pt'? 'pt-BR':'en-US').format(num);
}

export function formatDate(date: Date | string | number, locale: Locale, opts?: Intl.DateTimeFormatOptions){
  const d = date instanceof Date? date : new Date(date);
  return new Intl.DateTimeFormat(locale==='pt'?'pt-BR':'en-US', opts || { dateStyle: 'short'}).format(d);
}


